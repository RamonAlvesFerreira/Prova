package com.treinamento.moduloweb.modulo;

public class Modulo {

	private int id_modulo;
	private String nm_modulo;
	private String nm_instrutor1;
	private String nm_instrutor2;
	private String dt_inicio;
	
	
	public int getId_modulo() {
		return id_modulo;
	}
	public void setId_modulo(int id_modulo) {
		this.id_modulo = id_modulo;
	}
	public String getNm_modulo() {
		return nm_modulo;
	}
	public void setNm_modulo(String nm_modulo) {
		this.nm_modulo = nm_modulo;
	}
	public String getNm_instrutor1() {
		return nm_instrutor1;
	}
	public void setNm_instrutor1(String nr_instrutor1) {
		this.nm_instrutor1 = nr_instrutor1;
	}
	public String getDt_inicio() {
		return dt_inicio;
	}
	public void setDt_inicio(String dt_inicio) {
		this.dt_inicio = dt_inicio;
	}
	public String getNm_instrutor2() {
		return nm_instrutor2;
	}
	public void setNm_instrutor2(String nr_instrutor2) {
		this.nm_instrutor2 = nr_instrutor2;
	}

}
